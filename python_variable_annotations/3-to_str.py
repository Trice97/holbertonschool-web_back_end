#!/usr/bin/env python3
"""
This module provides string conversion operations.

It contains functions to convert floating point numbers to their
string representation for display and processing purposes.
"""


def to_str(n: float) -> str:
    """
    Return the string representation of a float number.
    This function takes a floating point number and converts it to
    its string representation using Python's built-in str() function.
    Args:
        n (float): The floating point number to convert to string
    Returns:
        str: The string representation of n
    """
    return str(n)
